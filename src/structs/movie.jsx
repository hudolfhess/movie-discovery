class MovieStruct {
  constructor(id, name, description, cover, release_date, vote_average, total_votes) {
    this.id = id
    this.name = name
    this.description = description
    this.cover = cover
    this.release_date = release_date
    this.vote_average = vote_average
    this.total_votes = total_votes
  }
}

export default MovieStruct
